package teachmeskills.homework3;

public class TaskLoop4 {
    public static void main(String[] args) {
        // With If-Else
        for (int i = 7; i < 100; i += 7) {
            System.out.print(i + " ");
        }

        // With while
        System.out.println();
        int i = 0;
        while (i <98) {
            i+=7;
            System.out.print(i + " ");
        }
    }
}
